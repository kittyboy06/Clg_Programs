#include<stdio.h>
#include<conio.h>
void swap(int *a, int *b) {
    int temp = *a;
    *a = *b;
    *b = temp;
}

int main() 
{
    int x, y;
    printf("Enter two integers to swap: ");
    scanf("%d %d", &x, &y);
    
    printf("Before swapping: x = %d, y = %d\n", x, y);
    swap(&x, &y);
    printf("After swapping: x = %d, y = %d\n", x, y);
    
    getch();
    return 0;
}